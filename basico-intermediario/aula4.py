# Tipos de dados 
# Tipos int e tipos float 
# int -> Numero inteiro
# o tipo int reapresenta qualquer numero
# positivo ou negativos. int sem sinal e considerado positivo 

print(11)  #int 
print(-11) #int
print(0)

# float -> Numero com pronto flutuante
# o tipo float representa qualquer positivo ou negativo com qualquer numero.
# float sem sinal e considerado positivo.

print(1.1, 10.11)  # int
print(0.0, -1.5)  # int


# A função type mostra o tipo que o Python inferiu ao valor.

print(type('Otevio'))
print(type(0))
print(type(1.1), type(-1.1), type(0.0))
